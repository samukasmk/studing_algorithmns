def count_chars_array_ord_stratetegy(s: str):
    char_counts_map = [0] * 256  # Initialize the list with zeros
    for char in s:
        char_counts_map[ord(char)] += 1  # Increment the count at the corresponding index
    return char_counts_map

def show_chars_count(char_counts_array):
    for index in range(len(char_counts_array)):
        if char_counts_array[index] > 0:
            print(f'({chr(index)},{index}): {char_counts_array[index]}')

